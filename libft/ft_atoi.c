int				ft_atoi(const char *str)
{
	int		signe;
	int		nb;
	int		i;

	i = 0;
	signe = 1;
	nb = 0;
	while (str && ((str[i] >= 9 && str[i] <= 13) || str[i] == ' '))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] < 58)
	{
		nb = (nb * 10) + str[i] - '0';
		i++;
	}
	return (nb * signe);
}
